namespace IODataPlatform.Models.ExcelModels;

/// <summary>
/// 硬件室典回资源库表
/// </summary>
public class 硬件室端接表 {

    /// <summary>
    /// 典回类型 
    /// </summary>
    public string LoopType { get; set; } = string.Empty;

    /// <summary>
    /// 版本 
    /// </summary>
    public string Version { get; set; } = string.Empty;

    /// <summary>
    /// 机组号 
    /// </summary>
    public string UnitNumber { get; set; } = string.Empty;

    /// <summary>
    /// 系统号 
    /// </summary>
    public string SystemNumber { get; set; } = string.Empty;

    /// <summary>
    /// 信号名称 
    /// </summary>
    public string SignalName { get; set; } = string.Empty;

    /// <summary>
    /// 扩展码 
    /// </summary>
    public string ExtensionCode { get; set; } = string.Empty;

    /// <summary>
    /// 信号位置 
    /// </summary>
    public string SignalLocation { get; set; } = string.Empty;

    /// <summary>
    /// 设备类型 
    /// </summary>
    public string DeviceType { get; set; } = string.Empty;

    /// <summary>
    /// 功能描述 
    /// </summary>
    public string FunctionDescription { get; set; } = string.Empty;

    /// <summary>
    /// 安全等级 
    /// </summary>
    public string SafetyLevel { get; set; } = string.Empty;

    /// <summary>
    /// 安全列 
    /// </summary>
    public string SafetyColumn { get; set; } = string.Empty;

    /// <summary>
    /// 应急供电 
    /// </summary>
    public string EmergencyPowerSupply { get; set; } = string.Empty;

    /// <summary>
    /// PAMS 
    /// </summary>
    public string PAMS { get; set; } = string.Empty;

    /// <summary>
    /// 端子组名 
    /// </summary>
    public string TerminalGroupName { get; set; } = string.Empty;

    /// <summary>
    /// 端子号P 
    /// </summary>
    public string TerminalNumberP { get; set; } = string.Empty;

    /// <summary>
    /// 端子号N 
    /// </summary>
    public string TerminalNumberN { get; set; } = string.Empty;

    /// <summary>
    /// 终端 
    /// </summary>
    public string Terminal { get; set; } = string.Empty;

    /// <summary>
    /// IO类型 
    /// </summary>
    public string IOType { get; set; } = string.Empty;

    /// <summary>
    /// 供电 
    /// </summary>
    public string PowerSupply { get; set; } = string.Empty;

    /// <summary>
    /// 回路电压 
    /// </summary>
    public string LoopVoltage { get; set; } = string.Empty;

    /// <summary>
    /// 盘号 
    /// </summary>
    public string PanelNumber { get; set; } = string.Empty;

    /// <summary>
    /// 横坐标 
    /// </summary>
    public string XCoordinate { get; set; } = string.Empty;

    /// <summary>
    /// 纵坐标 
    /// </summary>
    public string YCoordinate { get; set; } = string.Empty;
}
